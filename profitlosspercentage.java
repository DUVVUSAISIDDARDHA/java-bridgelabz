public class profitlosspercentage{
    public static void main(String[] args){
	
	int cp = 129;
	int sp = 191;
	
	int profit = sp - cp;
	double profit_percentage = (profit/cp)*100;
	
	System.out.println("The Cost Price is INR" + cp + "\n" + "Selling Price is INR" + sp + "\n" + "The Profit is INR" + profit + "\n" + " the ProfitPercentage is" + profit_percentage);
	}
}
	 